-- READ

SELECT USUARIOS_ID AS CLIENTE, FECHAINICIO AS FECHALLEGADA
FROM RESERVAS
WHERE USUARIOS_ID = 1
ORDER BY FECHALLEGADA;

-- UPDATE

UPDATE RESERVAS
SET FECHAINICIO = FECHAINICIO + 30
WHERE USUARIOS_ID = 1;

-- DELETE

DELETE
FROM RESERVAS
WHERE FECHAINICIO >= TO_DATE('2020-01-01', 'YYYY-MM-DD');